namespace Again.Runtime.ScriptImpoter
{
    public class SpineInfo
    {
        public float OffsetX;
        public float OffsetY;
        public float ScaleX;
        public float ScaleY;

        public SpineInfo(float offsetX, float offsetY, float scaleX, float scaleY)
        {
            OffsetX = offsetX;
            OffsetY = offsetY;
            ScaleX = scaleX;
            ScaleY = scaleY;
        }
    }
}